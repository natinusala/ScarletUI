/// Adapted from https://stackoverflow.com/questions/29421726/swift-how-to-get-the-string-before-a-certain-character
extension String {
    /// Returns the sub-string before the first occurence of the given character.
    func before(first delimiter: Character) -> String {
        if let index = firstIndex(of: delimiter) {
            let before = prefix(upTo: index)
            return String(before)
        }
        return self
    }

    /// Returns the sub-string after the first occurence of the given character.
    func after(first delimiter: Character) -> String {
        if let index = firstIndex(of: delimiter) {
            let after = suffix(from: index).dropFirst()
            return String(after)
        }
        return ""
    }
}
